package com.company;

public class Nota {
    String nombre;
    String apellido;
    String telefono;
    String correo;
    Boolean favorito;

    public Nota (String nombre, String apellido, String telefono, String correo, Boolean favorito ) {
        this.nombre = nombre;
        this.apellido = apellido;
        this.telefono = telefono;
        this.correo = correo;
        this.favorito = favorito;
    }
}
